package com.fti.recyclerview;

public class MovieModel {
    private String nameMovie;
    private String directorMovie;
    private int imageMovie;
    private String descMovie;

    public String getDescMovie() {
        return descMovie;
    }

    public void setDescMovie(String descMovie) {
        this.descMovie = descMovie;
    }

    public String getDirectorMovie() {
        return directorMovie;
    }

    public void setDirectorMovie(String directorMovie) {
        this.directorMovie = directorMovie;
    }

    public String getNameMovie() {
        return nameMovie;
    }

    public void setNameMovie(String nameMovie) {
        this.nameMovie = nameMovie;
    }

    public int getImageMovie() {
        return imageMovie;
    }

    public void setImageMovie(int imageMovie) {
        this.imageMovie = imageMovie;
    }

}
